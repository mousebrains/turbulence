"""Validate Python pipeline against MATLAB/ODAS reference output.

Compares Python PFile channel conversion, profile detection, fall rate,
and epsilon extraction against *_p2mat.nc files generated by
matlab/generate_odas_p2mat_nc.m.
"""

import os
from pathlib import Path

import numpy as np
import pytest

netCDF4 = pytest.importorskip("netCDF4")

pytestmark = pytest.mark.matlab

VMP_DIR = Path(__file__).parents[1] / "VMP"

# Discover all matched _p2mat.nc / .p pairs.
# MAX_MATLAB_FILES controls how many files to test (default 5, 0 = all).
_pairs = []
if VMP_DIR.exists():
    for nc_path in sorted(VMP_DIR.glob("*_p2mat.nc")):
        stem = nc_path.stem.replace("_p2mat", "")
        p_path = nc_path.with_name(stem + ".p")
        if p_path.exists():
            _pairs.append((p_path, nc_path))

_max = int(os.environ.get("MAX_MATLAB_FILES", "12"))
if _max and len(_pairs) > _max:
    _step = len(_pairs) / _max
    _pairs = [_pairs[int(i * _step)] for i in range(_max)]


def _file_id(pair):
    return pair[0].stem.split("_")[-1]


def _load_p2mat(nc_path: Path) -> dict:
    """Load a *_p2mat.nc file."""
    out = {}
    with netCDF4.Dataset(str(nc_path), "r") as ds:
        for attr in ds.ncattrs():
            out[attr] = ds.getncattr(attr)
        for vname in ds.variables:
            out[vname] = np.squeeze(ds.variables[vname][:])
    return out


@pytest.fixture(scope="module", params=_pairs, ids=[_file_id(p) for p in _pairs])
def file_pair(request):
    """Yield (PFile, nc_dict, p_path) for each matched file pair."""
    p_path, nc_path = request.param
    from rsi_python.p_file import PFile

    pf = PFile(p_path)
    ref = _load_p2mat(nc_path)
    return pf, ref, p_path


