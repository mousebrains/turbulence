    def test_epsilon_from_matlab_channels(self, eps_results):
        """Epsilon from Python pipeline should produce valid results."""
        ds = eps_results[0]
        eps_py = ds["epsilon"].values
        P_py = ds["P_mean"].values
        valid = np.isfinite(eps_py[0]) & (eps_py[0] > 0) & np.isfinite(P_py)
        assert np.sum(valid) > 5, "Not enough valid epsilon estimates"

    def test_matlab_python_temperature_consistency(self, file_pair):
        """Python T1 (slow, deconvolved) should match MATLAB T1_slow exactly."""
        pf, mat, _ = file_pair
        np.testing.assert_allclose(pf.channels["T1"], mat["T1_slow"], rtol=1e-10, atol=1e-10)
