class TestFastChannels:
    """Compare fast-rate channels.

    With deconvolution, T1_dT1 and T2_dT2 are now high-resolution
    fast-rate signals that should exactly match MATLAB's T1_fast / T2_fast.
    P_dP is slow-rate (deconvolved at slow rate), so P_fast is obtained
    by interpolating the deconvolved P to fast rate.
    """

    def test_pressure_fast(self, file_pair):
        """P interpolated to fast rate should match MATLAB P_fast."""
        pf, mat, _ = file_pair
        py_P_fast = np.interp(pf.t_fast, pf.t_slow, pf.channels["P"])
        ml_P_fast = mat["P_fast"]
        np.testing.assert_allclose(py_P_fast, ml_P_fast, rtol=1e-5, atol=0.05)

    def test_temperature_T1_fast(self, file_pair):
        """T1_dT1 (deconvolved fast-rate) should exactly match MATLAB T1_fast."""
        pf, mat, _ = file_pair
        np.testing.assert_allclose(pf.channels["T1_dT1"], mat["T1_fast"], rtol=1e-10, atol=1e-10)

    def test_temperature_T2_fast(self, file_pair):
        """T2_dT2 (deconvolved fast-rate) should exactly match MATLAB T2_fast."""
        pf, mat, _ = file_pair
        np.testing.assert_allclose(pf.channels["T2_dT2"], mat["T2_fast"], rtol=1e-10, atol=1e-10)

    def test_accelerometer_Ax(self, file_pair):
        """Ax should exactly match MATLAB Ax."""
        pf, mat, _ = file_pair
        np.testing.assert_allclose(pf.channels["Ax"], mat["Ax"], rtol=1e-10, atol=1e-10)

    def test_accelerometer_Ay(self, file_pair):
        """Ay should exactly match MATLAB Ay."""
        pf, mat, _ = file_pair
        np.testing.assert_allclose(pf.channels["Ay"], mat["Ay"], rtol=1e-10, atol=1e-10)

    def test_shear_conversion_factor(self, file_pair):
        """Python shear = MATLAB shear * speed^2 (ODAS divides by speed^2)."""
        pf, mat, _ = file_pair
        py_sh1 = pf.channels["sh1"]
        ml_sh1 = mat["sh1"]
        ml_speed = mat["speed_fast"]
        reconstructed = ml_sh1 * ml_speed**2
        mask = ml_speed > 0.1
        if np.any(mask):
            np.testing.assert_allclose(py_sh1[mask], reconstructed[mask], rtol=1e-6, atol=1e-6)


