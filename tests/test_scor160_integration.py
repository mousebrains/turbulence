# Integration tests for odas_tpw.scor160
"""End-to-end pipeline tests: L1→L2→L3→L4."""

import numpy as np
import pytest

from odas_tpw.scor160.io import L1Data, L2Params, L3Params
from odas_tpw.scor160.l2 import process_l2
from odas_tpw.scor160.l3 import process_l3
from odas_tpw.scor160.l4 import process_l4
from odas_tpw.scor160.nasmyth import nasmyth


def _synthetic_l1(
    n_time=50000,
    n_shear=2,
    n_vib=2,
    fs=512.0,
    epsilon=1e-8,
    nu=1.3e-6,
    speed=0.6,
):
    """Create a synthetic L1Data with Nasmyth-like shear signals.

    The shear signal is generated by filtering white noise through
    a spectral shape that approximates a Nasmyth spectrum, providing
    a more realistic test than pure white noise.
    """
    rng = np.random.default_rng(42)
    time = np.arange(n_time) / fs / 86400

    # Pressure ramp: 5 → 100 dbar
    pres = np.linspace(5, 100, n_time)

    # Generate shear with Nasmyth spectral shape
    # Create white noise, FFT, shape by sqrt(Nasmyth), inverse FFT
    shear = np.zeros((n_shear, n_time))
    freqs = np.fft.rfftfreq(n_time, 1 / fs)
    k = freqs / speed  # wavenumber
    nas_spec = nasmyth(epsilon, nu, k + 1e-30)
    nas_spec[0] = 0
    # Target PSD in frequency domain: Ψ(f) = Ψ(k) / W
    target_psd = nas_spec / speed
    # Amplitude filter: sqrt(PSD * df) where df = fs/n_time
    df = fs / n_time
    amplitude_filter = np.sqrt(np.maximum(target_psd * df, 0))

    for i in range(n_shear):
        noise = rng.standard_normal(n_time)
        noise_fft = np.fft.rfft(noise)
        shaped = noise_fft * amplitude_filter
        shear[i] = np.fft.irfft(shaped, n=n_time)

    # Vibration: small signal (sinusoidal + noise)
    vib = np.zeros((n_vib, n_time))
    t = np.arange(n_time) / fs
    for i in range(n_vib):
        vib[i] = 0.001 * np.sin(2 * np.pi * 20 * t) + 0.0005 * rng.standard_normal(n_time)

    return L1Data(
        time=time,
        pres=pres,
        shear=shear,
        vib=vib,
        vib_type="ACC",
        fs_fast=fs,
        f_AA=98.0,
        vehicle="vmp",
        profile_dir="down",
        time_reference_year=2024,
        temp=np.full(n_time, 10.0),
    )


